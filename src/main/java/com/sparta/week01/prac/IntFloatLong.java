package com.sparta.week01.prac;

public class IntFloatLong {

    public static void main(String[] args) {

        // String형 문자형
        String name = "김도현";
        System.out.println(name);

        // int형 숫자형
        int a = 7;

        // float형 숫자형
        float b = 3.7f;

        // Long형 숫자형
        Long c = 1000000000L;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}
